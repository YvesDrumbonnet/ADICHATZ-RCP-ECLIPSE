package org.adichatz.studio;

import org.adichatz.engine.common.LogBroker;
import org.adichatz.engine.indigo.AAdichatzUIPlugin;
import org.adichatz.engine.indigo.IndigoUIPlugin;
import org.adichatz.generator.common.GeneratorConstants;
import org.adichatz.scenario.ScenarioAdichatzApplication;
import org.adichatz.scenario.ScenarioPluginResources;
import org.adichatz.studio.db4o.Db4oTools;
import org.adichatz.studio.util.StudioLog;
import org.adichatz.studio.xjc.data.XjcDataAccess;
import org.eclipse.jface.resource.ImageDescriptor;
import org.osgi.framework.BundleContext;

// TODO: Auto-generated Javadoc
/**
 * The activator class controls the plug-in life cycle.
 */
public class StudioRcpPlugin extends AAdichatzUIPlugin {

	// The plug-in ID
	/** The Constant PLUGIN_ID. */
	public static final String PLUGIN_ID = GeneratorConstants.STUDIO_BUNDLE;

	// The shared instance
	/** The plugin. */
	private static StudioRcpPlugin plugin;

	protected void init() throws Exception {
		try {
			plugin = this;
			GeneratorConstants.ADICHATZ_STUDIO_PREFERENCE_STORE = getPreferenceStore();
			if (null == LogBroker.getLogger())
				new StudioLog();
			// Initilializes Adichatz Application
			ScenarioAdichatzApplication.initialize();
			pluginResources = new ScenarioPluginResources(GeneratorConstants.STUDIO_BUNDLE, GeneratorConstants.STUDIO_BUNDLE);
			pluginResources.setDataAccess(new XjcDataAccess(pluginResources));
			ScenarioAdichatzApplication.getInstance().setApplicationPluginResources(pluginResources);
			IndigoUIPlugin.ADICHATZ_APPLICATION_PLUGIN = this;
		} catch (Exception e) {
			startupLog(e);
			stop(context);
			System.exit(0);
		}
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see org.eclipse.ui.plugin.AbstractUIPlugin#stop(org.osgi.framework.BundleContext)
	 */
	public void stop(BundleContext context) throws Exception {
		Db4oTools.closeDB4oBase();
		plugin = null;
		super.stop(context);
	}

	/**
	 * Returns the shared instance.
	 * 
	 * @return the shared instance
	 */
	public static StudioRcpPlugin getDefault() {
		return plugin;
	}

	/**
	 * Returns an image descriptor for the image file at the given plug-in relative path.
	 * 
	 * @param path
	 *            the path
	 * @return the image descriptor
	 */
	public static ImageDescriptor getImageDescriptor(String path) {
		return imageDescriptorFromPlugin(PLUGIN_ID, path);
	}
}
